package com.implemica.test.test2;

public class NodeOfTarget {
    private String from;
    private String to;

    public NodeOfTarget(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public NodeOfTarget() {
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
